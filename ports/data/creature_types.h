// Creature / character type table for Sacred Gold, as a compile-time
// constexpr table. FUTURE-USE: this header is written but NOT yet wired into
// SacredSDK.vcxproj or any existing TU.
//
// SOURCE (authoritative, already verified):
//   - custom/lua/lib/data/creatures.lua  (474 rows, transcribed verbatim;
//     itself derived from refs/'sacred_modding - characters.csv').
//   - sdk/.claude/knowledge/re/data_tables.md  §1 ("creatures.lua").
//
// ID ENCODING (IMPORTANT):
//   The map KEY is the real creature type id (the CSV 'ID Dec' column) -- the
//   value the engine compares against (the cCreature type field). The CSV
//   'ID Hex' column is the BYTE-SWAPPED 16-bit form of that id:
//        id_hex = byteswap16(id) = ((id & 0xFF) << 8) | ((id >> 8) & 0xFF)
//   e.g. Seraphim id=1 -> 0x0100, Skeleton id=32 -> 0x2000,
//        Shaddar'Rim id=256 -> 0x0001.  Verified for all 474 rows (0 mismatch).
//
// BANDS (coarse category, derived from the id range; see data_tables.md):
//   hero          1..9     playable classes (Vampiress: 6 human / 7 vampire)
//   monster       32..255  hostile creatures & named bosses
//   humanoid_npc  256..368 humanoid NPCs (soldiers, mages, thieves, named)
//   animal        512..601 animals & mounts (wolves, horses, cows, bats)
//   townsfolk     640..    farmers, citizens, nobles, novices, quest NPCs
//
// NOTE: the byte-swap is a pure arithmetic identity, NOT an absolute engine VA,
// so no Sacred_decrypted.exe byte-signature check is required for this table.
//
// TODO(port): expose to a future engine::data namespace (engine::data::creatures).
// TODO(port): wire when the creature/NPC spawn UI or roster panel needs id->name.

