static_assert(kCreatureTypeCount == 474,
              "creature_types.h: expected 474 rows (see data_tables.md §1)");

// Sample compile-time validation of the byte-swap relationship for the table.
static_assert(byteswap16(1) == 0x0100, "byteswap16(1) should be 0x0100");
static_assert(byteswap16(32) == 0x2000, "byteswap16(32) should be 0x2000");
static_assert(byteswap16(256) == 0x0001, "byteswap16(256) should be 0x0001");

// Linear lookup by real creature type id ('ID Dec'). Returns nullptr if absent.
// (The table is id-ascending but sparse; a binary search is possible but a
// linear scan keeps this header dependency-free. TODO(port): switch to a
// std::lower_bound binary search if this is ever hot.)
inline const CreatureType* find_by_id(std::uint16_t id) {
  for (std::size_t i = 0; i < kCreatureTypeCount; ++i) {
    if (kCreatureTypes[i].id == id) return &kCreatureTypes[i];
  }
  return nullptr;
}

// Lookup by the byte-swapped 16-bit form ('ID Hex' column).
inline const CreatureType* find_by_hex(std::uint16_t hex) {
  return find_by_id(byteswap16(hex));
}

