// Coarse category band for a creature type id.
enum class CreatureBand : std::uint8_t {
  Hero = 0,
  Monster,
  HumanoidNpc,
  Animal,
  Townsfolk,
  Unknown,
};

// One creature type. `name` and `note` are static string literals (never null
// for `name`; `note` is nullptr when the source row had no note). `hex` is the
// byte-swapped 16-bit form of `id` (matches the CSV 'ID Hex' column).
struct CreatureType {
  std::uint16_t id;      // real creature type id ('ID Dec')
  std::uint16_t hex;     // byteswap16(id) ('ID Hex')
  CreatureBand  band;
  const char*   name;
  const char*   note;    // may be nullptr
};

// Pure arithmetic byte-swap of the low 16 bits (id <-> 'ID Hex' column).
constexpr std::uint16_t byteswap16(std::uint16_t v) {
  return static_cast<std::uint16_t>(((v & 0x00FFu) << 8) | ((v >> 8) & 0x00FFu));
}

