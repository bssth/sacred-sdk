// Coarse band a string-resource id belongs to (derived from its range).
enum class StringBand : std::uint8_t {
  MainQuest = 0,   // 17000..
  Underworld,      // 19500..
  Other,
};

// One string-resource entry. `name` is never null; `note` is nullptr unless the
// source CSV had a note. Ids are 16-bit-clean here (all <= 19600) but stored as
// u32 to match global.res handle width.
struct StringEntry {
  std::uint32_t id;
  const char*   name;
  const char*   note;   // may be nullptr
};

// Classify an id into its band by range (matches data_tables.md §5).
constexpr StringBand band_of(std::uint32_t id) {
  return (id >= 17000u && id < 18000u) ? StringBand::MainQuest
       : (id >= 19000u && id < 20000u) ? StringBand::Underworld
       : StringBand::Other;
}

