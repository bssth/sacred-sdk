// SacredSDK / ports / data / string_ids.h
//
// Quest / NPC / item string-resource id -> English display name for Sacred
// Gold, as a compile-time constexpr table. FUTURE-USE: written but NOT yet
// wired into SacredSDK.vcxproj or any existing TU.
//
// SOURCE (authoritative, already verified):
//   - custom/lua/lib/data/names.lua  (823 rows, verified; derived from
//     refs/'sacred_modding - names.csv').
//   - sdk/.claude/knowledge/re/data_tables.md  §5 ("names.lua").
//
// These are NOT procedural name fragments -- they are fixed string-resource
// entries the engine resolves for named NPCs, quest items, quest givers, and a
// handful of quest UI / status messages. Ids fall in two BANDS:
//   17000..17837  main-quest strings  (722 entries here)
//   19500..19600  Underworld strings  (101 entries here)
// Some entries carry a `note` such as "Forces model", "Unused", or a
// colour/variant hint (verbatim from the CSV).
//
// CAVEAT (see data_tables.md §5): these are LOCALIZED strings that, at runtime,
// should resolve through the engine's existing global.res string system rather
// than a compiled English table. This header is provided per the cluster spec
// for offline tooling / id->label lookup; prefer global.res for in-game text.
//
// ENCODING: this header is UTF-8 (a few names use non-ASCII letters, e.g.
//   17750 "Tari Telemmaite" with accented vowels). Compile with /utf-8 on MSVC.
//
// This is pure string data with no absolute engine VA, so no
// Sacred_decrypted.exe byte-signature check is required.
//
// TODO(port): expose to a future engine::data namespace (engine::data::strings).
// TODO(port): wire only if offline tooling needs id->label without global.res;
//             in-game text should still go through global.res.

